def compute_dur(units):
    d = units / 2 * PER_TIME * 7 / 8
    return max(int(d), 1)

def freq_from_idx(idx):
    idx = idx & 0x7F  # strip NO_EMPTY flag
    idx += FREQ_OFFSET
    if idx <= 0 or idx >= len(ORG_FREQ):
        return 0
    return ORG_FREQ[idx]

